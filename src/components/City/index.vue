<template>
  <div class="city_body">
    <div class="city_list">
      <Scroller ref="city_list">
        <!-- Scroller 包裹的元素必须为一个整体 -->
        <div>
          <div class="city_hot">
            <h2>热门城市</h2>
            <ul class="clearfix">
              <li
                v-for="item in hotList"
                :key="item.id"
                @tap="handleToCity(item.nm,item.id)"
              >{{item.nm}}</li>
            </ul>
          </div>
          <div class="city_sort" ref="city_sort">
            <div v-for="(item, index) in cityList" :key="index">
              <h2>{{item.index}}</h2>
              <ul>
                <li
                  v-for="(city,index) in item.list"
                  :key="city.id"
                  @tap="handleToCity(city.nm,city.id)"
                >{{city.nm}}</li>
              </ul>
            </div>
          </div>
        </div>
      </Scroller>
    </div>
    <div class="city_index">
      <ul>
        <li
          @touchstart="handleToIndex(index)"
          v-for="(cityP,index) in cityList"
          :key="cityP.index"
        >{{cityP.index}}</li>
      </ul>
    </div>
  </div>
</template>

<script>
export default {
  name: "City",
  data() {
    return {
      cityList: [],
      hotList: []
    };
  },
  mounted() {
    var cityList = window.localStorage.getItem("cityList");
    var hotList = window.localStorage.getItem("hotList");
    if (cityList && hotList) {
      this.cityList = JSON.parse(cityList);
      this.hotList = JSON.parse(hotList);
      return false;
    }
    this.$axios.get("/api/cityList").then(res => {
      var msg = res.data.mag;
      var cities = res.data.data.cities;
      var { cityList, hotList } = this.formatCityList(cities);
      this.cityList = cityList;
      this.hotList = hotList;
      window.localStorage.setItem("cityList", JSON.stringify(cityList));
      window.localStorage.setItem("hotList", JSON.stringify(hotList));
    });
  },
  methods: {
    formatCityList(cities) {
      var cityList = [];
      var hotList = [];
      for (let i = 0; i < cities.length; i++) {
        var firstLetter = cities[i].py.substring(0, 1).toUpperCase();
        if (toCom(firstLetter)) {
          //新添加
          cityList.push({
            index: firstLetter,
            list: [{ nm: cities[i].nm, id: cities[i].id }]
          });
        } else {
          for (let j = 0; j < cityList.length; j++) {
            if (cityList[j].index === firstLetter) {
              cityList[j].list.push({ nm: cities[i].nm, id: cities[i].id });
            }
          }
        }
        if (cities[i].isHot === 1) {
          hotList.push(cities[i]);
        }
      }
      function toCom(firstLetter) {
        for (let j = 0; j < cityList.length; j++) {
          if (cityList[j].index === firstLetter) {
            return false;
          }
        }
        return true;
      }
      cityList.sort((a, b) => {
        if (a.index > b.index) {
          return 1;
        } else if (a.index < b.index) {
          return -1;
        } else {
          return 0;
        }
      });
      return {
        cityList: cityList,
        hotList: hotList
      };
    },
    handleToIndex(index) {
      var h2 = this.$refs.city_sort.getElementsByTagName("h2");
      console.log(h2);
      // this.$refs.city_sort.parentNode.scrollTop = h2[index].offsetTop; //原生js跳转  因better-scroll管理元素原因 该方法失效  徐引入better-scroll跳转方法
      this.$refs.city_list.ToScrollTop(h2[index].offsetTop);
    },
    handleToCity(nm,id){
      this.$store.commit('City/CITY_INFO',{nm,id});
      window.localStorage.setItem('nowNm',nm);
      window.localStorage.setItem('nowId',id);
      this.$router.push('/movie/nowPlaying');
    }
  }
};
</script>

